-- Datasets
-- =============================================================
CREATE TABLE datasets (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id         UUID          NOT NULL,
  project_id        BIGINT        NOT NULL,
  name              VARCHAR(255)  NOT NULL,
  description       TEXT,
  source            VARCHAR(30)   NOT NULL,
  schema_version_id BIGINT        NOT NULL,
  latest_version_id BIGINT,
  active_version_id BIGINT,
  total_rows        INT           NOT NULL DEFAULT 0,
  status            VARCHAR(20)   NOT NULL DEFAULT 'DRAFT',
  created_by        BIGINT        NOT NULL,
  created_at        TIMESTAMP WITH TIME ZONE NOT NULL,
  updated_at        TIMESTAMP WITH TIME ZONE NOT NULL,
  deleted_at        TIMESTAMP WITH TIME ZONE,

  CONSTRAINT uk_datasets_public_id UNIQUE (public_id),
  CONSTRAINT fk_datasets_project   FOREIGN KEY (project_id) REFERENCES projects (id),
  CONSTRAINT fk_datasets_schema    FOREIGN KEY (schema_version_id) REFERENCES project_schemas (id),
  CONSTRAINT fk_datasets_creator   FOREIGN KEY (created_by) REFERENCES users (id)
);

CREATE INDEX idx_datasets_project ON datasets (project_id);

CREATE TABLE dataset_versions (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id         UUID          NOT NULL,
  dataset_id        BIGINT        NOT NULL,
  schema_version_id BIGINT        NOT NULL,
  version_number    INT           NOT NULL,
  source            VARCHAR(30)   NOT NULL,
  status            VARCHAR(20)   NOT NULL DEFAULT 'DRAFT',
  total_rows        INT           NOT NULL DEFAULT 0,
  valid_rows        INT           NOT NULL DEFAULT 0,
  invalid_rows      INT           NOT NULL DEFAULT 0,
  created_by        BIGINT        NOT NULL,
  created_at        TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),

  CONSTRAINT uk_dataset_versions_public_id UNIQUE (public_id),
  CONSTRAINT uk_dataset_versions_number UNIQUE (dataset_id, version_number),
  CONSTRAINT fk_dataset_versions_dataset FOREIGN KEY (dataset_id) REFERENCES datasets (id),
  CONSTRAINT fk_dataset_versions_schema FOREIGN KEY (schema_version_id) REFERENCES project_schemas (id),
  CONSTRAINT fk_dataset_versions_creator FOREIGN KEY (created_by) REFERENCES users (id)
);

CREATE INDEX idx_dataset_versions_dataset ON dataset_versions (dataset_id);

ALTER TABLE datasets
  ADD CONSTRAINT fk_datasets_latest_version FOREIGN KEY (latest_version_id) REFERENCES dataset_versions (id),
  ADD CONSTRAINT fk_datasets_active_version FOREIGN KEY (active_version_id) REFERENCES dataset_versions (id);

CREATE TABLE dataset_rows (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id           UUID          NOT NULL,
  dataset_id          BIGINT        NOT NULL,
  dataset_version_id  BIGINT        NOT NULL,
