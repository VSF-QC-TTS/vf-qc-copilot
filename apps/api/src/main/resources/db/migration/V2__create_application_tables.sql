  CONSTRAINT fk_dataset_rows_dataset FOREIGN KEY (dataset_id) REFERENCES datasets (id) ON DELETE CASCADE,
  CONSTRAINT fk_dataset_rows_version FOREIGN KEY (dataset_version_id)
    REFERENCES dataset_versions (id) ON DELETE CASCADE
);

CREATE INDEX idx_dataset_rows_dataset ON dataset_rows (dataset_id);
CREATE INDEX idx_dataset_rows_version ON dataset_rows (dataset_version_id);

