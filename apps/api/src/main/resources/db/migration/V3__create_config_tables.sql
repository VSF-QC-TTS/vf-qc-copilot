-- =============================================================
-- Target Config
-- =============================================================
CREATE TABLE target_configs (
  id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id                UUID          NOT NULL,
  project_id               BIGINT        NOT NULL,
  version                  INT           NOT NULL DEFAULT 1,
  name                     VARCHAR(255),
  method                   VARCHAR(10)   NOT NULL,
  url                      TEXT          NOT NULL,
  headers                  JSONB,
  query_params             JSONB,
  body_template            TEXT,
  response_path            VARCHAR(500),
  timeout_ms               INT           NOT NULL DEFAULT 30000,
  request_field_snapshot   JSONB,
  response_field_snapshot  JSONB,
  raw_curl                 TEXT,
  last_test_status         VARCHAR(30),
  last_tested_at           TIMESTAMP WITH TIME ZONE,
  created_at               TIMESTAMP WITH TIME ZONE NOT NULL,
  updated_at               TIMESTAMP WITH TIME ZONE NOT NULL,

  CONSTRAINT uk_target_configs_public_id UNIQUE (public_id),
  CONSTRAINT uk_target_configs_project   UNIQUE (project_id),
  CONSTRAINT fk_target_configs_project   FOREIGN KEY (project_id) REFERENCES projects (id)
);

CREATE INDEX idx_target_configs_project ON target_configs (project_id);

-- =============================================================
-- Secret Refs (shared for Target headers + Judge API keys)
-- =============================================================
CREATE TABLE secret_refs (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id        UUID          NOT NULL,
  project_id       BIGINT        NOT NULL,
  owner_type       VARCHAR(50)   NOT NULL,
  owner_id         BIGINT        NOT NULL,
  secret_name      VARCHAR(255)  NOT NULL,
  secret_location  VARCHAR(50)   NOT NULL,
  secret_path      VARCHAR(500),
  encrypted_value  TEXT          NOT NULL,
  masked_value     VARCHAR(255)  NOT NULL,
  created_at       TIMESTAMP WITH TIME ZONE NOT NULL,
  updated_at       TIMESTAMP WITH TIME ZONE NOT NULL,

  CONSTRAINT uk_secret_refs_public_id UNIQUE (public_id),
  CONSTRAINT fk_secret_refs_project   FOREIGN KEY (project_id) REFERENCES projects (id)
);

CREATE INDEX idx_secret_refs_owner ON secret_refs (owner_type, owner_id);

-- =============================================================
