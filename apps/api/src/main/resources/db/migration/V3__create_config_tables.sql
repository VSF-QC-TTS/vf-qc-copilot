CREATE INDEX idx_judge_configs_project ON judge_configs (project_id);

-- =============================================================
-- Dataset Schema
-- =============================================================
CREATE TABLE dataset_schema_versions (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id   UUID   NOT NULL,
  project_id  BIGINT NOT NULL,
  version     INT    NOT NULL DEFAULT 1,
  created_at  TIMESTAMP WITH TIME ZONE NOT NULL,

  CONSTRAINT uk_schema_versions_public_id UNIQUE (public_id),
  CONSTRAINT fk_schema_versions_project   FOREIGN KEY (project_id) REFERENCES projects (id)
);

CREATE INDEX idx_schema_versions_project ON dataset_schema_versions (project_id);

CREATE TABLE dataset_columns (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id         UUID          NOT NULL,
  schema_version_id BIGINT        NOT NULL,
  column_name       VARCHAR(255)  NOT NULL,
  display_name      VARCHAR(255),
  data_type         VARCHAR(30)   NOT NULL,
  role              VARCHAR(30)   NOT NULL,
  required          BOOLEAN       NOT NULL DEFAULT false,
  sample_value      TEXT,
  description       TEXT,
  display_order     INT           NOT NULL DEFAULT 0,
  created_at        TIMESTAMP WITH TIME ZONE NOT NULL,
  updated_at        TIMESTAMP WITH TIME ZONE NOT NULL,

  CONSTRAINT uk_dataset_columns_public_id UNIQUE (public_id),
  CONSTRAINT uk_dataset_columns_name UNIQUE (schema_version_id, column_name),
  CONSTRAINT fk_dataset_columns_schema FOREIGN KEY (schema_version_id)
    REFERENCES dataset_schema_versions (id) ON DELETE CASCADE
);

CREATE INDEX idx_dataset_columns_schema ON dataset_columns (schema_version_id);

