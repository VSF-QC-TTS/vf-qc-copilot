CREATE TABLE dataset_versions (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id         UUID          NOT NULL,
  dataset_id        BIGINT        NOT NULL,
  schema_version_id BIGINT        NOT NULL,
  version_number    INT           NOT NULL,
  source            VARCHAR(30)   NOT NULL,
  status            VARCHAR(20)   NOT NULL DEFAULT 'DRAFT',
  total_rows        INT           NOT NULL DEFAULT 0,
  valid_rows        INT           NOT NULL DEFAULT 0,
  invalid_rows      INT           NOT NULL DEFAULT 0,
  created_by        BIGINT        NOT NULL,
  created_at        TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),

  CONSTRAINT uk_dataset_versions_public_id UNIQUE (public_id),
  CONSTRAINT uk_dataset_versions_number UNIQUE (dataset_id, version_number),
  CONSTRAINT fk_dataset_versions_dataset FOREIGN KEY (dataset_id) REFERENCES datasets (id),
  CONSTRAINT fk_dataset_versions_schema FOREIGN KEY (schema_version_id) REFERENCES project_schemas (id),
  CONSTRAINT fk_dataset_versions_creator FOREIGN KEY (created_by) REFERENCES users (id)
);

CREATE INDEX idx_dataset_versions_dataset ON dataset_versions (dataset_id);

ALTER TABLE datasets
  ADD COLUMN latest_version_id BIGINT,
  ADD COLUMN active_version_id BIGINT,
  ADD COLUMN deleted_at TIMESTAMP WITH TIME ZONE;

ALTER TABLE datasets
  ADD CONSTRAINT fk_datasets_latest_version FOREIGN KEY (latest_version_id) REFERENCES dataset_versions (id),
  ADD CONSTRAINT fk_datasets_active_version FOREIGN KEY (active_version_id) REFERENCES dataset_versions (id);

ALTER TABLE dataset_rows
  ADD COLUMN public_id UUID,
  ADD COLUMN dataset_version_id BIGINT,
  ADD COLUMN validation_status VARCHAR(20) NOT NULL DEFAULT 'VALID',
  ADD COLUMN validation_errors JSONB;

ALTER TABLE dataset_rows
  ADD CONSTRAINT uk_dataset_rows_public_id UNIQUE (public_id);

ALTER TABLE dataset_rows
  ADD CONSTRAINT fk_dataset_rows_version FOREIGN KEY (dataset_version_id)
    REFERENCES dataset_versions (id) ON DELETE CASCADE;

ALTER TABLE dataset_rows
  DROP CONSTRAINT IF EXISTS uk_dataset_rows_index;

CREATE INDEX idx_dataset_rows_version ON dataset_rows (dataset_version_id);
CREATE UNIQUE INDEX uk_dataset_rows_version_index
  ON dataset_rows (dataset_version_id, row_index)
  WHERE dataset_version_id IS NOT NULL;

CREATE TABLE dataset_jobs (
  id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id          UUID          NOT NULL,
  project_id         BIGINT        NOT NULL,
  dataset_id         BIGINT,
  dataset_version_id BIGINT,
  type               VARCHAR(30)   NOT NULL,
  status             VARCHAR(30)   NOT NULL DEFAULT 'QUEUED',
  progress           INT           NOT NULL DEFAULT 0,
  message            TEXT,
  error_message      TEXT,
  payload            JSONB,
  result             JSONB,
  created_by         BIGINT        NOT NULL,
  created_at         TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  updated_at         TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now(),
  completed_at       TIMESTAMP WITH TIME ZONE,

  CONSTRAINT uk_dataset_jobs_public_id UNIQUE (public_id),
  CONSTRAINT fk_dataset_jobs_project FOREIGN KEY (project_id) REFERENCES projects (id),
  CONSTRAINT fk_dataset_jobs_dataset FOREIGN KEY (dataset_id) REFERENCES datasets (id),
  CONSTRAINT fk_dataset_jobs_version FOREIGN KEY (dataset_version_id) REFERENCES dataset_versions (id),
  CONSTRAINT fk_dataset_jobs_creator FOREIGN KEY (created_by) REFERENCES users (id)
);

CREATE INDEX idx_dataset_jobs_dataset ON dataset_jobs (dataset_id);
CREATE INDEX idx_dataset_jobs_project ON dataset_jobs (project_id);
